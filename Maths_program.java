//WAP on different types of Math methods
package area_for_Scanner;

public class Maths_program {

	public static void main(String[] args) {
		System.out.println(Math.addExact(10, 20));
		System.out.println(Math.subtractExact(100, 50));
		System.out.println(Math.max(10, 20));
		System.out.println(Math.max(5.5, 6.5));
		System.out.println(Math.min(200, 500));
		System.out.println(Math.min(4.5, 7.5));
		System.out.println(Math.multiplyExact(30, 70));
		System.out.println(Math.sqrt(9));
		
		double pi_value=Math.PI;
		
		System.out.println(pi_value);
		
	
	}

}
